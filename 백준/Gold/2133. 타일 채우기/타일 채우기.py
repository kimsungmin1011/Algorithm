n = int(input())

# dp[i] : 2 x i 크기의 직사각형을 타일로 채우는 경우의 수
dp = [0] * (n + 1)

# 아무것도 없는 2 x 0 크기(빈 상태)를 1가지 경우로 본다 (점화식 편의를 위해)
dp[0] = 1

# 2 x 2 크기를 채우는 경우의 수는 3가지
# 1) 세로 도미노 2개
# 2) 가로 도미노 2개
# 3) 특수한 정사각형 모양 (문제에서 허용하는 패턴)

# i는 짝수 길이만 의미가 있으므로 2씩 증가시키며 채운다
for i in range(2, n + 1, 2):
    # 1) 오른쪽 끝에 2 x 2 기본 패턴 3가지 중 하나를 붙이는 경우
    #    => 왼쪽 2 x (i-2)를 채우는 경우의 수 * 3
    dp[i] = dp[i - 2] * 3

    # 2) 오른쪽에 '특수한 모양'이 한 번 등장하는 경우들
    #    특수 모양 왼쪽 크기를 j(짝수)라고 하며, j는 0, 2, ..., i-4 까지 가능
    #    각 j마다 특수 패턴이 2가지씩 존재하므로 dp[j] * 2 를 더해준다
    for j in range(0, i - 2, 2):  # j = 0, 2, 4, ..., i-4
        dp[i] += dp[j] * 2

# n이 홀수라면 dp[n]은 기본값 0 그대로이므로 자동으로 0 출력
print(dp[n])
