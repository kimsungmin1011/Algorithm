n = int(input())

# dp[i][0]: i번째 행에 사자를 놓지 않는 경우의 수
# dp[i][1]: i번째 행 왼쪽 칸에 사자를 놓는 경우의 수
# dp[i][2]: i번째 행 오른쪽 칸에 사자를 놓는 경우의 수
dp = [[0] * 3 for _ in range(n + 1)]

# 초기값 설정
dp[1][0] = 1  # 1번째 줄에 사자를 놓지 않음
dp[1][1] = 1  # 1번째 줄에 왼쪽 칸에 사자
dp[1][2] = 1  # 1번째 줄에 오른쪽 칸에 사자

# n이 1인 경우(초기값으로 충분함)와 n>=2인 경우를 구분
for i in range(2, n + 1):
    dp[i][0] = (dp[i - 1][0] + dp[i - 1][1] + dp[i - 1][2]) % 9901
    dp[i][1] = (dp[i - 1][0] + dp[i - 1][2]) % 9901
    dp[i][2] = (dp[i - 1][0] + dp[i - 1][1]) % 9901

result = (dp[n][0] + dp[n][1] + dp[n][2]) % 9901
print(result)
